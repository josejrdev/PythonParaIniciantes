# Programa que faz várias perguntas ao usuário
# Perguntando o nome
nome = input("Qual é o seu nome? ")
# Perguntando a idade
idade = int(input("Qual é a sua idade? "))
# Perguntando o email
email = input("Qual o seu email? ")
# Exibindo as informações na tela
print("Seu nome é %s. A sua idade é %d anos de idade. Seu email é %s :)" % (nome, idade, email))